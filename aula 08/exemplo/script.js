// funções - functions

// como declarar
// deve ser declarada usando a palavra function seguida de "()" e "{}"

function nomeDaFuncao() { 
    console.log('mensagem de uma função');
}
// chamando a função
nomeDaFuncao();

// função com parâmetros - definindo parâmetros
function mensagem(primeiro, segundo) {
    console.log(primeiro, segundo);
}

//chamando a função e atribuindo valores aos parâmetros
mensagem('Tudo certo', 'jovem!');